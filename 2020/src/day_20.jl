using AdventOfCode
using Test, BenchmarkTools

input = readlines("2020/data/day_20.txt")

function parse_images(input)
    images = Dict{Int, BitArray{2}}()
    for i in 1:12:length(input)
        id = parse(Int, input[i][6:end-1])
        image = ==('#').(permutedims(reduce(hcat, collect.(input[i+1:i+10]))))
        images[id] = image
    end
    return images
end

function extract_borders(image)
    m, n = size(image)
    right = view(image, :, n)
    top = view(image, 1, :)
    left = view(image, :, 1)
    bottom = view(image, m, :)
    return right, top, left, bottom
end

isnotempty(x) = !isempty(x)

function part_1(input)
    ims = parse_images(input)
    borders = Dict(id => extract_borders(image) for (id, image) in ims)
    nfits(id) = sum(max(count(isnotempty, intersect(borders[id], borders[id2])), 
                        count(isnotempty, intersect(reverse.(borders[id]), borders[id2])))
                    for id2 in setdiff(keys(borders), id))
    return prod(filter(x -> nfits(x) == 2, keys(borders)))
end
